// This module is pure evil. Pure, hacky, evil. All ye who value safety turn
// back now. I do not endorse or suggest the use of any of the methods
// described here.
//
// Basically, it's faster to access that which is in the data segment than
// that which is on the heap. But, if the value you want to make a global
// variable is bigger than what the stack can hold, lazy_static! will be of no
// use to you. See, lazy_static! requires that you return the value that the
// global variable should be set to. This means you *have* to put the value on
// the stack. So instead... this.

// This type is something like 30 megabytes. Literally no stack on any OS is
// that big by default, so we have to more or less improvise our own version
// of lazy_static in order to get RAW SPEED.
static mut PIECES: Option<[Tile; 21]> = None;

static INIT: Once = ONCE_INIT;

#[inline]
fn initialize() {
    unsafe {
        INIT.call_once(|| {
            // Here's a problem. We have to assign PIECES to be Some without
            // actually putting anything of PIECES's size on the stack. To do
            // that, we have to somehow change PIECES from None to Some without
            // actually initializing any memory in that Some. How might we do
            // this?
            //
            // Well, if you've ever looked at the internal memory
            // representation of an Option<T>, you might note that the Some
            // representation just kinda sticks a 1 at the front of the data,
            // while the None representation is zeroed. So behold, the ultimate
            // in unsafe.
            
            unsafe {
                {
                    // We cast a pointer to PIECES to be a mutable pointer to
                    // the first byte of PIECES.
                    let disgusting: *mut u8 = mem::transmute(&mut PIECES);
                    
                    // Then we set the first bit to be 1. This makes PIECES
                    // a Some value without ever actually putting it on the
                    // stack. Honestly I'm shocked this works.
                    let first = ptr::read(disgusting);
                    ptr::write(disgusting, first | 1);
                }

                let mut n = 0;

                for (piece, shape) in pieces_unsafe().iter_mut().zip(PIECE_SHAPES.iter()) {
                    *piece = Tile::new(*shape, n);
                    n += 1;
                }
            }
        });
    }
}

#[inline]
unsafe fn pieces_unsafe() -> &'static mut [Tile; 21] {
    // This is always called after PIECES has been initialized (and while it's
    // being initialized). Because of this, to avoid the overhead of the match,
    // we quietly pretend that there's a 0% chance of PIECES being None when 
    // this function is called.

    match PIECES {
        Some(ref mut array) => array,
        _ => unreachable(),
    }
}

#[inline]
fn pieces() -> &'static [Tile; 21] {
    // This is safe, since it initializes first.
    initialize();

    unsafe {
        match PIECES {
            Some(ref array) => array,
            _ => unreachable(),
        }
    }
}

#[inline]
pub fn piece(which: usize) -> &'static Tile {
    &pieces()[which]
}

#[inline]
pub fn iter() -> ::std::slice::Iter<'static, Tile> {
    pieces().iter()
}

